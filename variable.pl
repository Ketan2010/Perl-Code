#perl variable can store string,integer,float,ocatl and hexadecimal number
#string
$string="this is string\n";
print $string;
#integer
$integer=12345;
print "this is integer: $integer \n";
#float
$float=12345.789;
print "this is floating number: $float \n";
#octal : start with 0
#below line will print octal number in its decimal value
$octal=0753;
print "this is octal number:$octal \n";
#hexadecimal :start with 0x
#below line will print hex number in its decimal value
$hex=0xabc;
print "hexadecimal number: $hex \n";

### OUTPUT ###
#this is string
#this is integer: 12345
#this is floating number: 12345.789
#this is octal number:491
#hexadecimal number: 2748